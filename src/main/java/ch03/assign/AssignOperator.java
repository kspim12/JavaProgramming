package ch03.assign;

public class AssignOperator {

    public static void main(String[] args) {
        int i1 = 10;
        i1 += 20;
        System.out.println(i1);
        i1 -= 10;
        System.out.println(i1);
        i1 *= 10;
        System.out.println(i1);
        i1 /= 10;
        System.out.println(i1);
        i1 %= 2;
        System.out.println(i1);

    }
}
